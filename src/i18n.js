let currentLocale = 'es';

function template(str, vars = {}) {
  return str.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? ''));
}

export function getInitialLocale() {
  const saved = localStorage.getItem('sensagotchi_locale');
  if (saved && dictionaries[saved]) return saved;
  const lang = (navigator.language || 'en').toLowerCase();
  return lang.startsWith('es') ? 'es' : 'en';
}

export function setLocale(locale) {
  currentLocale = dictionaries[locale] ? locale : 'es';
  localStorage.setItem('sensagotchi_locale', currentLocale);
}

export function getLocale() {
  return currentLocale;
}

export function t(key, vars) {
  const value = key.split('.').reduce((obj, part) => obj?.[part], dictionaries[currentLocale]);
  if (typeof value !== 'string') return key;
  return template(value, vars);
}

export function actionLabel(name) {
  return dictionaries[currentLocale].events[name]?.name || name.replace(/_/g, ' ');
}

export function eventDisplay(name, human, rawBlockedReason = '', rawNote = '') {
  const entry = dictionaries[currentLocale].events[name] || {};
  let blocked = entry.blocked || rawBlockedReason;
  if (name === 'orgasm') {
    blocked = human.prolactin >= 30 ? entry.blockedRefractory : entry.blockedNeedArousal;
  }
  if (name === 'exercise') {
    blocked = human.time_since_exercise < 24
      ? template(entry.blockedCooldown || rawBlockedReason, { hours: Math.ceil(24 - human.time_since_exercise) })
      : (entry.blockedEnergy || rawBlockedReason);
  }

  const noteMap = {
    'SSRIs dulling arousal & dopamine response': t('ui.note_ssri_dulling'),
    'inhibition dampening response': t('ui.note_inhibition'),
  };

  return {
    name: entry.name || name.replace(/_/g, ' '),
    description: entry.description || '',
    blocked,
    note: noteMap[rawNote] || rawNote,
  };
}

